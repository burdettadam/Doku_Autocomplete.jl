function doku_encode(set::Array{Char,1})
    # strategy, to save space and memory, take every possible 3 combinations
    # of [1-9], which happens to be 84, and represent them as an ascii char.
    # this will take a normal row, [123456789] to something like [!.@.$], saving space.
    @inbounds _a = set[1:3]
    @inbounds _b = set[4:6]
    @inbounds _c = set[7:9]
    a = encoding[ join(_a) ]
    b = encoding[ join(_b) ]
    c = encoding[ join(_c) ]
    return [a,b,c]
end

function doku_decode(encoded_row::Array{Char,1})
    result = Array{Char,1}(undef,9)
    @inbounds result[1:3] = decoding[encoded_row[1]]
    @inbounds result[4:6] = decoding[encoded_row[2]]
    @inbounds result[7:9] = decoding[encoded_row[3]]
    return result
end